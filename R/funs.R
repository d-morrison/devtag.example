#' @import devtag
NULL

#' add
#'
#' @param x numeric
#' @param y numeric
#'
#' @export
add <- function(x, y) {
  x + y
}

#' subtract
#'
#' @param x numeric
#' @param y numeric
#' @dev
subtract <- function(x, y) {
  x - y
}

#' multiply
#'
#' @param x numeric
#' @param y numeric
#' @noRd
multiply <- function(x, y) {
  x * y
}

